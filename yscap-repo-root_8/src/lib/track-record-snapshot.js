'use strict';

/* The borrower's SAVED STATIC COPY of their track record: a self-contained
   HTML file (readable data + a deep link that reopens the live builder with
   the same deals) generated by the static tool's portal bridge on every
   change. ONE current copy per borrower — it lives on the profile as a
   borrower-scoped document and is what the Profile section, every file's
   experience condition, and staff all download. */

const db = require('../db');
const storage = require('./storage');

const DOC_KIND = 'track_record_html';
const MAX_BYTES = 4 * 1024 * 1024;   // a rendered track record is tens of KB; 4 MB is a hard stop

async function saveSnapshot(borrowerId, { html, filename, uploadedByKind, uploadedById }) {
  const text = String(html || '');
  if (!text.trim()) { const e = new Error('html required'); e.status = 400; throw e; }
  const buf = Buffer.from(text, 'utf8');
  if (buf.length > MAX_BYTES) { const e = new Error('snapshot too large'); e.status = 413; throw e; }
  const name = String(filename || 'Track_Record.html').replace(/[^\w.\- ]+/g, '_').slice(0, 120) || 'Track_Record.html';
  const { ref, provider } = await storage.save(buf, { filename: name });
  const r = await db.query(
    `INSERT INTO documents (borrower_id,filename,content_type,size_bytes,storage_provider,storage_ref,
                            uploaded_by_kind,uploaded_by_id,doc_kind,source_type,visibility)
     VALUES ($1,$2,'text/html',$3,$4,$5,$6,$7,$8,'system','borrower') RETURNING id, created_at`,
    [borrowerId, name, buf.length, provider, ref, uploadedByKind, uploadedById, DOC_KIND]);
  // one current copy per borrower — the previous snapshot is superseded
  await db.query(
    `UPDATE documents SET is_current=false, review_status='superseded'
      WHERE borrower_id=$1 AND doc_kind=$2 AND id<>$3 AND is_current`,
    [borrowerId, DOC_KIND, r.rows[0].id]);
  return { documentId: r.rows[0].id, filename: name, updatedAt: r.rows[0].created_at };
}

async function latestSnapshot(borrowerId) {
  const r = await db.query(
    `SELECT id AS "documentId", filename, created_at AS "updatedAt"
       FROM documents
      WHERE borrower_id=$1 AND doc_kind=$2 AND is_current
      ORDER BY created_at DESC LIMIT 1`,
    [borrowerId, DOC_KIND]);
  return r.rows[0] || null;
}

module.exports = { saveSnapshot, latestSnapshot, DOC_KIND };
